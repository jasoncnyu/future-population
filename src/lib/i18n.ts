function formatMessage(template: string, params?: Record<string, string | number>) {
  if (!params) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => String(params[key] ?? ""));
}

export function t(locale: Locale, key: string, params?: Record<string, string | number>): string {
  const msg = messages[locale]?.[key] ?? messages[defaultLocale]?.[key] ?? key;
  return formatMessage(msg, params);
}

export function normalizeLocale(input?: string | null): Locale | null {
  if (!input) return null;
  const lowered = input.toLowerCase();
  const exact = supportedLocales.find((l) => l === lowered);
  if (exact) return exact;
  const base = lowered.split("-")[0];
  return (supportedLocales.find((l) => l === base) ?? null) as Locale | null;
}

export function localeLabel(locale: Locale): string {
  try {
    const dn = new Intl.DisplayNames([locale], { type: "language" });
    return dn.of(locale) ?? locale;
  } catch {
    return locale;
  }
}
